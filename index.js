function add(a,b) {
  return parseInt(a + b)
}

function subtract(a,b) {
  return parseInt(a - b)
}

function multiply(a,b) {
  return parseInt(a * b)
}

function divide(a,b) {
  return parseInt(a / b)
}



function inc(n) {
  return parseFloat(n++)
}

function dec(n) {
  return parseFloat(n--)
}

function makeInt(n) {
  return parseInt(n)
}

